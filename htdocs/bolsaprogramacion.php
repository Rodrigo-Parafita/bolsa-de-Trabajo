    <script>
        // Agrega este script al final del body para asegurarte de que el DOM se haya cargado
        
        // Obtén referencias a los menús desplegables
        const schoolDropdown = document.querySelector("#schoolDropdown");
        const professionDropdown = document.querySelector("#professionDropdown");
        const promedioDropdown = document.querySelector("#promedioDropdown");
        
        // Agrega manejadores de eventos para los menús desplegables
        schoolDropdown.addEventListener("change", filterResults);
        professionDropdown.addEventListener("change", filterResults);
        promedioDropdown.addEventListener("change", filterResults);
        
        // Obtén todos los elementos de resultados
        const results = document.querySelectorAll(".card");
        
        // Función para filtrar los resultados
        function filterResults() {
            const selectedSchool = schoolDropdown.value;
            const selectedProfession = professionDropdown.value;
            const selectedPromedio = promedioDropdown.value;
            
            results.forEach(result => {
                const school = result.dataset.school;
                const profession = result.dataset.profession;
                const promedio = result.dataset.promedio;
                
                // Comprueba si el resultado cumple con los filtros seleccionados
                const schoolMatch = selectedSchool === "All" || selectedSchool === school;
                const professionMatch = selectedProfession === "All" || selectedProfession === profession;
                const promedioMatch = selectedPromedio === "All" || selectedPromedio === promedio;
                
                // Muestra u oculta el resultado según los filtros
                if (schoolMatch && professionMatch && promedioMatch) {
                    result.style.display = "block";
                } else {
                    result.style.display = "none";
                }
            });
        }
    </script>

